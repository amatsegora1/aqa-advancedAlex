let firstPersonName = 'Alex';
let secondPersonName = 'Bill';

//Concatenation
let greetingsConcatenation = 'Hello,' + ' ' + firstPersonName + ' and ' + secondPersonName + ' happy to see you guys!';
console.log(greetingsConcatenation);

//Template Literals
let greetingsTemplate = `Hello, ${firstPersonName} and ${secondPersonName} happy to see you guys!` //'Hello,' + ' ' + firstPersonName + ' and ' + secondPersonName + ' happy to see you guys!';
console.log(greetingsTemplate);